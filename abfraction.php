                    <h2>ABFRACTIONS - WHAT ARE THEY?</h2>
                    <a href="">By admin / June 25, 2015 / Featured / 0 Comment / likes 0</a>

                    <p class="pt-4">Do your find yourself having more and more sensitivity to all elements? Though
                        sensitivity can be
                        caused by numerous reasons, but a common cause might be something that is more preventable than
                        you might think. Until recently, abfraction was thought to be caused by improper toothbrushing
                        techniques.
                    </p>
                    <p>An abfraction lesion is now known to be the loss of tooth structure seen clinically on the cheek
                        side of a tooth near and often under the gumline. It is identified by its sharply demarcated
                        horizontal notch-like appearance. While a cavity may develop in the area due to the exposed
                        dentin, the decay process is not responsible for the abfraction.
                    </p>
                    <div style="float: right; margin: 10px 20px;">
                        <img src="assets/images/deep-abfraction-notches.jpg" alt="">
                    </div>
                    <div>
                        <h6>The Cause</h6>
                        <p>Abfraction is caused when two opposing teeth, one top and one bottom tooth, meet improperly
                            during chewing and nonfunctional clenching or tooth grinding habits. At that point of
                            stress, the tooth will “flex” very slightly. When a bite relationship is not correct, teeth
                            undergo forces of compression and tension from the opposing teeth. In this way, the tooth is
                            flexed slightly every time the teeth fully close.
                        </p>
                        <p>This chronic flexing of the tooth causes a loss of tooth substance (enamel and dentin) at the
                            gumline where the enamel is the thinnest. Enamel is brittle and cannot easily bend. As the
                            tooth is moved back and forth (because the bite is off or because you grind your teeth),
                            pieces of enamel fracture. The dentin, or underlying tooth structure, is more elastic and
                            can bend slightly. The bending of the tooth will slowly damage the tooth, and a notch will
                            eventually become visible.
                        </p>
                        <p>One characteristic of abfraction that can make diagnosis difficult is that it may be
                            partially or entirely under the gum tissue. At one time, it was thought that this type of
                            tooth notching was caused by improper brushing habits, such as forcefully scrubbing back and
                            forth against the neck of the tooth. While it is true that you can actually wear a notch in
                            the tooth by brushing too hard in a see-saw fashion, the first tissue to wear away under
                            those conditions is the gum tissue. Unlike abfraction, abrasion occurs above the gumline. In
                            contrast, an abfraction occurs many times under the gum, or is partially covered by gum
                            tissue.
                        </p>
                    </div>

                    <div>
                        <h6>Treatment</h6>
                        <p>In most cases, abfraction can be treatment in a short procedure. A tooth-colored, bonded
                            resin material is often placed to restore the notched tooth, and is a relatively easily
                            restoration to accomplish. If the notch extends deep under the gumline, the tissue may have
                            to be removed to expose the extent of the notched area in order to restore it.
                        </p>
                        <h6>Preventing Abfraction</h6>
                        <p>Abfraction caused by teeth flexing can be prevented with the use of a mouthguard(day or
                            night). Lab-made guards are a great way to add a barrier between your upper and lower teeth
                            to prevent grinding and clenching but is only effective if patients are able to use it
                            daily. If a mouthguard sounds like an unattractive solution, using Botulinum toxin(Botox)
                            injection can be a highly effective solution as well. Botox injected directly into the jaw
                            muscle can help relax the muscle in the area that causes the tension in your jaw. To learn
                            more about Botox injection, <a href="">click here.</a>
                        </p>
